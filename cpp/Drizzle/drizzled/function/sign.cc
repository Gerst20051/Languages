#include <config.h>

#include <drizzled/function/sign.h>

namespace drizzled
{

int64_t Item_func_sign::val_int()
{
  assert(fixed == 1);
  double value= args[0]->val_real();
  null_value=args[0]->null_value;
  return value < 0.0 ? -1 : (value > 0 ? 1 : 0);
}


} /* namespace drizzled */
